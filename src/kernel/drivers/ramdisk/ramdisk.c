static void
ramdisk_driver_thread(void){
	static Msg m;
	while(true){
		receive( ANY, &m );
		switch( m.type ){
			uint32_t i;
			uint8_t data;
			case DEV_READ:
				/* m.buf is the request thread's buffer, this server
				 * write directly to that buffer */
				for( i=0; i < m.len; i++){
					if( m.offset + i >= NR_MAX_SIZE ) assert(0);
					data = *(disk + m.offset + i);
					*(uint8_t*)(m.buf + i) = data;
				}
				m.ret = i;
				m.dest = m.src;
				m.src = RAMDISK;
				send( m.dest, &m );
				break;
			default:	assert(0);
		}
	}
}
