pid_t RAMDISK;
static void ramdisk_driver_thread(void);

void init_ramdisk(void){
	PCB *p = create_kthread( ramdisk_driver_thread );
	RAMDISK = p->pid;
	wakeup(p);
	hal_register("ram", RAMDISK, 0);
}

static void
ramdisk_driver_thread(void){
	static Msg m;
	while(true){
		receive( ANY, &m );
		switch( m.type ){
			uint32_t i;
			case DEV_READ:
				lock();
				// printk("ramdisk read!\n");
				unlock();
				/* m.buf is the request thread's buffer, this server
				 * write directly to that buffer */
        uint32_t req_pid = m.req_pid;
        void *buf = m.buf;
        uint32_t offset = m.offset;
        uint32_t len = m.len;
        uint32_t end = offset + len;
        if(offset / NR_FILE_SIZE != end / NR_FILE_SIZE){
          /* out of file bound */
          assert(0);
        }
        /* read page by page */
        uint32_t num = len % PAGE_SIZE == 0 ? (len / PAGE_SIZE) : ((len / PAGE_SIZE) + 1);
				for( i = 0; i < num; i++){
          if(i == num - 1){
            len = end - offset;
          }else{
            len = PAGE_SIZE;
          } 
          PCB *pcb = fetch_pcb(req_pid);
          copy_from_kernel_mine(pcb, buf, disk + offset, len);
          buf += len;
          offset += len;
				}
				m.ret = i;
				m.dest = m.src;
				m.src = RAMDISK;
				send( m.dest, &m );
				// lock();
				// printk("ramdisk read finished\n");
				// unlock();
				break;
			default:
      	assert(0);
		}
	}
}
